// Arithmetic Operators in javascript

let num1 = 5;
let num2 = 6;

// Addition: adds num1 and num2
console.log("\n", num1 + num2); // Output: 11

// Subtraction: subtracts num2 from num1
console.log("\n", num1 - num2); // Output: -1

// Multiplication: multiplies num1 and num2
console.log("\n", num1 * num2); // Output: 30

// Division: divides num1 by num2
console.log("\n", num1 / num2); // Output: 0.833...

// Modulus: returns the remainder of num1 divided by num2
console.log("\n", num1 % num2); // Output: 5